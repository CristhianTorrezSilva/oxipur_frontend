import React from 'react';

const Badge = ({ children, variant = 'default' }) => {

    const variants = {
        default: "bg-gray-100 text-gray-800",
        success: "bg-green-100 text-green-800 border border-green-200", // Para "Validado", "Entregado"
        warning: "bg-yellow-100 text-yellow-800 border border-yellow-200", // Para "Pendiente", "En Ruta"
        danger: "bg-red-100 text-red-800 border border-red-200", // Para "Rechazado", "Urgente"
        info: "bg-blue-100 text-blue-800 border border-blue-200" // Para "Nuevo", "Info"
    };

    return (
        <span className={`px-2.5 py-0.5 rounded-full text-xs font-semibold ${variants[variant]}`}>
      {children}
    </span>
    );
};

export default Badge;